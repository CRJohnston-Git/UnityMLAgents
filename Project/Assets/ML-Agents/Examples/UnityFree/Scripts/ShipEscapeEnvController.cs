    // Update is called once per frame
    private void FixedUpdate()
    {
        m_ResetTimer += 1;
        if (MaxEnvironmentSteps > 0)
        {
            // 여기서 inactive 시켜준다. - 이유는 마지막 done에서 inactive 된 애를 빼고 계산한다. reward 계산에서 문제가 있을 것으로 예상.(마지막 애만 제대로 학습 안되는 현상)
            foreach (RiseAgent red in inactive_reds)
            {
                red.gameObject.SetActive(false);
            }
            inactive_reds.Clear();
            //if(need_done == true && m_ResetTimer >= 500) // 최소 500스텝은 기다리게 하자.
            if (need_done == true)
            {
                redGroup.EndGroupEpisode();
                blueGroup.EndGroupEpisode();
                ResetScene();
            }
            else if (m_ResetTimer >= MaxEnvironmentSteps)
            {
                redGroup.GroupEpisodeInterrupted();
                blueGroup.GroupEpisodeInterrupted();
                ResetScene();
            }
        }
    }

